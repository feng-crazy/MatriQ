#!/bin/bash
# MatriQ 测试运行脚本

set -e

echo "=========================================="
echo "MatriQ 测试套件"
echo "=========================================="

# 检查是否在虚拟环境中
if [ -z "$VIRTUAL_ENV" ]; then
    echo "警告: 建议在虚拟环境中运行测试"
    read -p "是否继续? (y/n) " -n 1 -r
    echo
    if [[ ! $REPLY =~ ^[Yy]$ ]]; then
        exit 1
    fi
fi

# 运行测试
echo ""
echo "运行所有测试..."
pytest "$@"

echo ""
echo "=========================================="
echo "测试完成"
echo "=========================================="

